# Create string variable
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog!."

# Manipulate string with replace method
sentence_manipulated = sentence.replace("!", " ")

# Print manipulated string
print(sentence_manipulated)

# Print string with uppercase method
print(sentence_manipulated.upper())

# Print string reversed with string manipulation
print(sentence_manipulated.upper()[::-1])